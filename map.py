def draw_map(window):
    m_width = len(map[0])
    m_height = len(map)
    block_width = MAP_WIDTH / m_width
    block_height = MAP_HEIGHT / m_height
    row_pos = 0
    column_pos = 0

    for row in map:
        for column in row:
            if column == 0:
                colour = BLACK
            elif column == 1:
                colour = WHITE
            pygame.draw.rect(window, colour, (column_pos * block_width, row_pos * block_height, block_width - 1, block_height - 1))
            column_pos += 1
        column_pos = 0
        row_pos += 1
